void main()
{
  // Switch Case
  // هي طريقة شبية لحالة if

  // Football : 1
  // Basketball : 2
  // Tennis : 3

  var x=2;
  String y;


  switch(x){
    case 1 :
      y="Football";
      break;
    case 2 :
      y="Basketball";
      break;
    case 3 :
      y="Tennis";
      break;
    default:
      y="U Don't have favorite";

  }
  print(y);
  // Basketball
  // أنتَ هنا بتقوله لمَّا يكون إختياره x كذا يكون الإجابة بتاعته كذا
}