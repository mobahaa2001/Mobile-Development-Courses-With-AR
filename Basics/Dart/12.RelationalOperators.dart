void main()
{
  // Relational Operators
  // هو للسأل غن أكبر مِن أصغر مِن.... إلخ

  // > Greater than
  // < Lesser than
  // >= Greater than or equal
  // <= Lesser than or equal
  // == equality
  // != not equal

  int x = 10;
  int y = 8;

  bool z = x < y;
  print(z);
  // false

  z = x > y;
  print(z);
  // true

  z = x == y;
  print(z);
  // false

  z = x >= y;
  print(z);
  // true

  z = x<= y;
  print(z);
  // false

  z = x != y;
  print(z);
  // true

}