void main()
{
  // Final & Const
  // هنا ندرس شيئين متشابهين في أنَّهُم ثابتين لا نستطيع أنْ نغيرهم
  // الفرق بينهم
  // final is run time
  // هو أنَّهُ سيقوم بمراجعة الأمر أو البيانات عندما نقوم بتشغيلهُ

  final x=4;

  // const is compile time
  // هو إنَّهُ لنْ ينتظر إذا كنت ستقوم بتشغلاهُ أو لا فدائمًا يقوم بِمُراجعت البيانات أول بأول
  const pi = 3.14;

  // الـ const أفضل وأسرع مِن final إذا كانت البيانات ثابته
}